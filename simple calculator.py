a=int(input("Enter first number : "))
b=int(input("Enter second number : "))
choice =input("Enter your choice +,-,*,/ : ")
if choice == "+":
    print(a+b)

elif choice == "-":
    print(a-b)
elif choice == "*":
    print(a*b)
elif choice == "/":
    print(a/b)
else:
    print("invalid choice")
    



 
